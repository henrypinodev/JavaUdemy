package POOInterfacesRepositorio.Ejercicio2;

public class Libro  {

    private String titulo, autor;

    public Libro(String titulo, String autor) {
        this.titulo = titulo;
        this.autor = autor;
    }

    public String getTitulo() {
        return titulo;
    }

    public String getAutor() {
        return autor;
    }


}
